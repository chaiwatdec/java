package java_hw8_Practice_Collection_1;

public class Doc
{
	public String docname, docdesp;

	public Doc()
	{

	}

	public Doc(String docname, String docdesp)
	{
		this.docname = docname;
		this.docdesp = docdesp;
	}

	public String getName()
	{
		return docname;
	}

	public String getDescription()
	{
		return docdesp;
	}
}
